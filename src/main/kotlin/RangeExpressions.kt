fun main()
{
    for(i in 1..10)
    {
        print("$i"+" ")
    }
    println()

    for(x in 'a'..'z')
    {
        print(x)
    }
    println()

    for(i in 15 downTo 1)
    {
        print("$i"+" ")
    }
    println()

    for(j in 1..20 step 2)
    {
        print("$j"+" ")
    }
    println()


    for (x in 1.rangeTo(5)) {
        print("$x"+" ")
    }
    println()


    for(x in 1 until 15)
    {
        print("$x"+" ")
    }
    println()
}